	md.WriteString(fmt.Sprintf("- **Images:** %d\n", assets.Images))
	md.WriteString(fmt.Sprintf("- **Documents:** %d\n", assets.Documents))
	md.WriteString(fmt.Sprintf("- **Other Resources:** %d\n\n", assets.OtherResources))

